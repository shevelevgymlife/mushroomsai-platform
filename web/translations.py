def parse_accept_language(header: str) -> str:
    """Parse Accept-Language header and return best supported lang code."""
    if not header:
        return "ru"
    langs = []
    for part in header.split(","):
        part = part.strip()
        if ";q=" in part:
            code, q = part.split(";q=", 1)
            try:
                langs.append((code.strip()[:2].lower(), float(q)))
            except ValueError:
                pass
        elif part:
            langs.append((part[:2].lower(), 1.0))
    langs.sort(key=lambda x: -x[1])
    for code, _ in langs:
        if code in SUPPORTED_LANGS:
            return code
    return "ru"


